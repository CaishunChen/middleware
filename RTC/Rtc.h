typedef struct
{
	uint16_t Year;
	uint8_t Month;
	uint8_t Day;
	uint8_t Hour;
	uint8_t Minutes;
	uint8_t Seconds;
}rtc_date_t;

typedef enum
{
	RTC_OK = 0,
	RTC_WRONG_PARAMETER,
	RTC_NO_ALARM_SET
}rtc_status_t;

typedef void (* rtc_alarm_callback_t)(void);

